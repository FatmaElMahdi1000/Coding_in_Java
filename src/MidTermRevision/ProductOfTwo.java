package MidTermRevision;

public class ProductOfTwo {

    public static void main(String[] args)
    {
        int product = 0;
        int num1 = 5;
        int num2 = 5;
        for(int i = 0; i < num2; i++)
        {
            product+= num1;
        }
        System.out.println(product);
    }
}
